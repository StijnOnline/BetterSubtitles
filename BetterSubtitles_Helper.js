function ParseTimeStamp(t){
    //Format = 00:02:04,743 to seconds as number
    return Number(t.split(':')[0]) * 60 * 60 + //hours
            Number(t.split(':')[1]) * 60 + //minutes
            Number(t.split(':')[2].split(',')[0]) + //seconds
            Number(t.split(',')[1]) * 0.001; //miliseconds
}

